package com.example.evaluationkotlin

class Calculus {
    fun add (a: Int, b: Int): Int{
        return a + b
    }

    fun substract(a: Int,b: Int): Int{
        return a - b
    }

    fun multiply(a:Int,b: Int):Int{
        return a * b
    }

    fun divide(a: Int,b: Int): Int{
        return a / b
    }

    fun modulo(a: Int,b: Int):Int{
        return a % b
    }
}